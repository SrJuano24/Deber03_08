    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (JOptionPane.showConfirmDialog(this, "¿Crear el personaje?", "Seleccione una opción...", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {

            var retorno = new String[7];

            retorno[0] = this.jTextField4.getText();
            retorno[1] = this.jTextField6.getText();
            retorno[2] = this.jTextField7.getText();
            retorno[3] = this.jComboBox2.getSelectedItem().toString();

            if (this.jRadioButton1.isSelected()) {
                retorno[4] = "Una estrella";

            } else {
                if (this.jRadioButton2.isSelected()) {
                    retorno[4] = "Dos estrellas";
                } else {
                    if (this.jRadioButton3.isSelected()) {
                        retorno[4] = "Tres estrellas";
                    } else {
                        if (this.jRadioButton4.isSelected()) {
                            retorno[4] = "Cuatro estrellas";

                        } else {
                            if (this.jRadioButton6.isSelected()) {
                                retorno[4] = "Cinco estrellas";
                            } else {
                            }
                        }
                    }
                }
            }

            retorno[5] = this.jTextField5.getText();
            retorno[6] = this.jTextField8.getText();
            try {
                this.personajeControl.crear(retorno);
                this.actualizarTabla();
                JOptionPane.showMessageDialog(rootPane, "El personaje se creó  exitosamente");
            } catch (Exception e1) {
                JOptionPane.showMessageDialog(this, e1.getMessage(), "Error al crear personaje", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

